Termos e Condições - Geographic World

Bem-vindo aos Termos e Condições de uso do site Geographic World. Agradecemos por escolher nossa plataforma para acessar informações sobre o conteudo de geografia. Ao utilizar nossos serviços, você concorda com os seguintes termos e condições:

Privacidade e Proteção de Dados:

a. Não Compartilhamento de Dados: O Geographic World está empenhado em proteger a privacidade de nossos usuários. Não compartilhamos informações de usuários com terceiros, a menos que seja exigido por lei.

b. Conservação de Dados: Não removemos intencionalmente informações do banco de dados, como comentários ou contas excluídas, e futuramente, as postagens tambem continuarão no banco de dados após a exclusão. Isso é feito para fins de transparência e rastreabilidade. No entanto, nenhum dado confidencial é exibido publicamente.

c. Criptografia de Dados Sensíveis: Todos os dados sensíveis dos usuários são criptografados usando o algoritmo SHA-256 no banco de dados. A administração não tem a capacidade de descriptografar esses dados de maneira alguma.

d. Protocolo HTTPS Obrigatório: No Geographic World, a segurança dos seus dados é de extrema importância. Portanto, todo o acesso e a transmissão de dados entre o cliente e o servidor devem ser obrigatoriamente feitos por meio do protocolo HTTPS (HyperText Transfer Protocol Secure) para garantir a criptografia e a segurança dos dados em trânsito, O uso do protocolo HTTPS é fundamental para proteger a confidencialidade e integridade dos dados durante a comunicação entre o cliente e o servidor. Isso ajuda a proteger suas informações contra interceptação e acesso não autorizado.

