a. Banimento de Contas: Qualquer infração, como atividades ilegais ou não conformes com nossos termos, está sujeita ao banimento da conta. No entanto, os dados de usuários continuam armazenados em nosso banco de dados após o banimento, mas não são mais exibidos publicamente.

b. Denúncia às Autoridades: Qualquer atividade ilegal cometida por um usuário em nosso site será relatada às autoridades locais, e cooperaremos integralmente em investigações legais.

Cadastro e Verificação de E-mail:

a. Verificação Obrigatória: Para criar uma conta no Geographic World, é obrigatória a verificação do endereço de e-mail fornecido durante o processo de registro. Isso é feito para garantir a autenticidade e segurança das contas de usuário.

b. Restrição de Informações Únicas: É estritamente proibido ter dois ou mais usuários com o mesmo nome de usuário (username), endereço de e-mail ou ID de uso interno (UserID). Cada conta deve ter informações exclusivas para garantir a individualidade de cada usuário.

